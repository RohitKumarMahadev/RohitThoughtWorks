export async function getBookShelfPreview() {
  const resArray = await Promise.all([
    fetch("https://covers.openlibrary.org/b/isbn/9781451648539.json"),
    fetch("https://covers.openlibrary.org/b/isbn/9781501109799.json"),
    fetch("https://covers.openlibrary.org/b/isbn/9780812993257.json"),
    fetch("https://covers.openlibrary.org/b/isbn/9780393355628.json"),
    fetch("https://covers.openlibrary.org/b/isbn/9780062315007.json"),
  ]);
  const titles = [
    "Steve Jobs",
    "Pre-suasion",
    "The Road to Character",
    "Surely, You'r Joking, Mr.Feynman",
    "The Alchemist",
  ];
  const authors = [
    "Walter Issacson",
    "Rober Cladini",
    "David Brooks",
    "Richard P.Feynman",
    "Paulo Coelho",
  ];
  const booksCollection = await Promise.all(resArray.map((res) => res.json()));
  const books = booksCollection.map((obj, index) => ({
    ...obj,
    author: authors[index],
    filename: titles[index],
  }));
  return books;
}
